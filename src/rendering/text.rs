use super::hud_render::HudVertex;

/// Bitmap font atlas for monospace text rendering.
///
/// Each printable ASCII character (32..=126) is stored as an 8x12 pixel glyph
/// in a single-channel texture atlas.
pub struct BitmapFont {
    pub texture: wgpu::Texture,
    pub texture_view: wgpu::TextureView,
    pub char_width: f32,
    pub char_height: f32,
    pub atlas_cols: u32,
}

/// Width/height of each glyph in pixels.
const GLYPH_W: u32 = 8;
const GLYPH_H: u32 = 12;

/// Number of printable ASCII characters: 32 (' ') through 126 ('~') inclusive = 95.
const CHAR_COUNT: u32 = 95;

/// Number of columns in the atlas texture.
const ATLAS_COLS: u32 = 16;

impl BitmapFont {
    /// Create a new bitmap font. Generates the atlas procedurally.
    pub fn new(device: &wgpu::Device, queue: &wgpu::Queue) -> Self {
        let atlas_rows = (CHAR_COUNT + ATLAS_COLS - 1) / ATLAS_COLS;
        let atlas_w = ATLAS_COLS * GLYPH_W;
        let atlas_h = atlas_rows * GLYPH_H;

        // Generate RGBA pixel data for the atlas.
        let mut pixels = vec![0u8; (atlas_w * atlas_h * 4) as usize];

        for char_idx in 0..CHAR_COUNT {
            let ch = (char_idx + 32) as u8 as char;
            let glyph = get_glyph_bitmap(ch);

            let col = char_idx % ATLAS_COLS;
            let row = char_idx / ATLAS_COLS;
            let base_x = col * GLYPH_W;
            let base_y = row * GLYPH_H;

            for gy in 0..GLYPH_H.min(glyph.len() as u32) {
                let bits = glyph[gy as usize];
                for gx in 0..GLYPH_W {
                    let set = (bits >> (7 - gx)) & 1;
                    if set != 0 {
                        let px = base_x + gx;
                        let py = base_y + gy;
                        let idx = ((py * atlas_w + px) * 4) as usize;
                        pixels[idx] = 255;     // R
                        pixels[idx + 1] = 255; // G
                        pixels[idx + 2] = 255; // B
                        pixels[idx + 3] = 255; // A
                    }
                }
            }
        }

        let texture_size = wgpu::Extent3d {
            width: atlas_w,
            height: atlas_h,
            depth_or_array_layers: 1,
        };

        let texture = device.create_texture(&wgpu::TextureDescriptor {
            label: Some("bitmap_font_atlas"),
            size: texture_size,
            mip_level_count: 1,
            sample_count: 1,
            dimension: wgpu::TextureDimension::D2,
            format: wgpu::TextureFormat::Rgba8UnormSrgb,
            usage: wgpu::TextureUsages::TEXTURE_BINDING | wgpu::TextureUsages::COPY_DST,
            view_formats: &[],
        });

        queue.write_texture(
            wgpu::TexelCopyTextureInfo {
                texture: &texture,
                mip_level: 0,
                origin: wgpu::Origin3d::ZERO,
                aspect: wgpu::TextureAspect::All,
            },
            &pixels,
            wgpu::TexelCopyBufferLayout {
                offset: 0,
                bytes_per_row: Some(atlas_w * 4),
                rows_per_image: Some(atlas_h),
            },
            texture_size,
        );

        let texture_view = texture.create_view(&wgpu::TextureViewDescriptor::default());

        Self {
            texture,
            texture_view,
            char_width: GLYPH_W as f32,
            char_height: GLYPH_H as f32,
            atlas_cols: ATLAS_COLS,
        }
    }

    /// Get UV coordinates for a character: [u_min, v_min, u_max, v_max].
    pub fn get_char_uv(&self, c: char) -> [f32; 4] {
        let idx = if (c as u32) >= 32 && (c as u32) <= 126 {
            (c as u32) - 32
        } else {
            0 // space for unknown chars
        };

        let atlas_rows = (CHAR_COUNT + ATLAS_COLS - 1) / ATLAS_COLS;
        let atlas_w = (ATLAS_COLS * GLYPH_W) as f32;
        let atlas_h = (atlas_rows * GLYPH_H) as f32;

        let col = idx % ATLAS_COLS;
        let row = idx / ATLAS_COLS;

        let u_min = (col * GLYPH_W) as f32 / atlas_w;
        let v_min = (row * GLYPH_H) as f32 / atlas_h;
        let u_max = ((col + 1) * GLYPH_W) as f32 / atlas_w;
        let v_max = ((row + 1) * GLYPH_H) as f32 / atlas_h;

        [u_min, v_min, u_max, v_max]
    }

    /// Build vertex data to render a string at screen position (x, y).
    /// `scale` multiplies the glyph size. `color` is RGBA.
    /// Returns 6 vertices (two triangles) per character.
    pub fn build_text_vertices(
        &self,
        text: &str,
        x: f32,
        y: f32,
        scale: f32,
        color: [f32; 4],
    ) -> Vec<HudVertex> {
        let gw = self.char_width * scale;
        let gh = self.char_height * scale;
        let mut verts = Vec::with_capacity(text.len() * 6);

        for (i, c) in text.chars().enumerate() {
            let [u0, v0, u1, v1] = self.get_char_uv(c);
            let cx = x + i as f32 * gw;
            let cy = y;

            // Two triangles: top-left origin, Y increasing downward (screen space).
            // Triangle 1: TL, TR, BR
            verts.push(HudVertex { position: [cx, cy], uv: [u0, v0], color });
            verts.push(HudVertex { position: [cx + gw, cy], uv: [u1, v0], color });
            verts.push(HudVertex { position: [cx + gw, cy + gh], uv: [u1, v1], color });
            // Triangle 2: TL, BR, BL
            verts.push(HudVertex { position: [cx, cy], uv: [u0, v0], color });
            verts.push(HudVertex { position: [cx + gw, cy + gh], uv: [u1, v1], color });
            verts.push(HudVertex { position: [cx, cy + gh], uv: [u0, v1], color });
        }

        verts
    }
}

